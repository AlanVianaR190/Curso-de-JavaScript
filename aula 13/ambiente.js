//laço while
let contador = 1

while (contador <= 10){
    console.log(`${contador}`);
    contador += 1
}

console.log();

//laço do/while
let contador2 = 0

do{
    console.log(`${contador2}`);
    contador2 += 1
}while(contador2 < 11)

console.log();

//laço for
for(let c = 1; c < 11; c += 1){
    console.log(`${c}`);
}

